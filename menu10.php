<h1>A growing tide in Europe says again: "GMOs, no thank you!"</h1>
Blogpost by Timothy Eden - 16 November, 2015 at 16:451 comment
<h3><p>Despite years of pressure and efforts by the European Union political elite in favour of genetically engineered (GE) crops, European citizens (all of you) and national governments from a broad range of political backgrounds have won a major victory: 17 European countries and 4 European regions have chosen to ban GE crops. <br>For those in the rest of the world who are fighting large-scale industrial and chemical agriculture – and the GE crops that abet it – this victory gives hope. Now they see hope for equitable and sustainable solutions such as ecological farming. <br>
 <img class=eco src="http://www.greenpeace.org/eu-unit/Global/eu-unit/image/2015/20151006%20Map%20GMO%20cultivation%20opt%20out.jpg">
<p>These 17 European countries and 4 regions have now clearly decided with legal determination that no GE crops will be planted in the foreseeable future in their territories. Together these countries comprise over two-thirds of the arable land of Europe and represent 65% of EU population. <br>This decisive majority demonstrates how public opposition to GE foods is a growing tide in Europe and around the world. <br>
There are many reasons for Europe and the world to remain sceptical about GE crops as outlined in our most recent report: Twenty Years of Failure: Why GM crops have failed to deliver on their promises. <br>
The European opposition to GE crops is a huge victory over the corporate control of our food production. It is the explicit and strong reaffirmation of the right for people everywhere in the world to choose the food they want to eat and the type of agriculture they want to encourage. In essence, it is about ecological farming, food sovereignty and healthy and nutritious food.  <br>
Greenpeace believes in a system of farming based on science, innovation, knowledge and biodiversity. <br>We call this eco farming. Eco farming allows us to confront modern challenges, such as climate change, while preserving environmental integrity and protecting health. <br>
<img class=grow src="http://www.greenpeace.org/international/community_images/84/2284/119731_202261.jpg">
 
GE is the wrong approach to food and agriculture because it encourages monocultures, intensification, and chemicals-use while undermining biodiversity. <br>GE also shifts the power away from small and medium-size farmers to the major corporations and their patented genetic materials. <br> These are all elements of the broken food system we fight against. A new farming system with people and farmers at its heart is the way to go. <br>
Timothy Eden is a Greenpeace Volunteer. </h3>



