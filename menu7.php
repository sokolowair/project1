<h1>Renewable Energy</h1><br>
<p><h3>There are many forms of renewable energy . Most of these renewable energies depend in one way or another on sunlight. Wind and hydroelectric power are the direct result of differential heating of the Earth's surface which leads to air moving about (wind) and precipitation forming as the air is lifted. <br>Solar energy is the direct conversion of sunlight using panels or collectors. Biomass energy is stored sunlight contained in plants. Other renewable energies that do not depend on sunlight are geothermal energy, which is a result of radioactive decay in the crust combined with the original heat of accreting the Earth, and tidal energy, which is a conversion of gravitational energy. <br>
Solar. <br> <p>This form of energy relies on the nuclear fusion power from the core of the Sun. <br> This energy can be collected and converted in a few different ways. The range is from solar water heating with solar collectors or attic cooling with solar attic fans for domestic use to the complex technologies of direct conversion of sunlight to electrical energy using mirrors and boilers or photovoltaic cells. <br>Unfortunately these are currently insufficient to fully power our modern society.
Wind Power. <br><p> The movement of the atmosphere is driven by differences of temperature at the Earth's surface due to varying temperatures of the Earth's surface when lit by sunlight. <br> Wind energy can be used to pump water or generate electricity, but requires extensive areal coverage to produce significant amounts of energy. <br>
Hydroelectric energy. <br><p>This form uses the gravitational potential of elevated water that was lifted from the oceans by sunlight. It is not strictly speaking renewable since all reservoirs eventually fill up and require very expensive excavation to become useful again. At this time, most of the available locations for hydroelectric dams are already used in the developed world. <br>
Biomass is the term for energy from plants. <br> Energy in this form is very commonly used throughout the world. Unfortunately the most popular is the burning of trees for cooking and warmth. This process releases copious amounts of carbon dioxide gases into the atmosphere and is a major contributor to unhealthy air in many areas. <br>Some of the more modern forms of biomass energy are methane generation and production of alcohol for automobile fuel and fueling electric power plants. <br>
Hydrogen and fuel cells. <br><p>These are also not strictly renewable energy resources but are very abundant in availability and are very low in pollution when utilized. Hydrogen can be burned as a fuel, typically in a vehicle, with only water as the combustion product. <br>This clean burning fuel can mean a significant reduction of pollution in cities. Or the hydrogen can be used in fuel cells, which are similar to batteries, to power an electric motor. <br>In either case significant production of hydrogen requires abundant power. Due to the need for energy to produce the initial hydrogen gas, the result is the relocation of pollution from the cities to the power plants. <br> There are several promising methods to produce hydrogen, such as solar power, that may alter this picture drastically. <br>
Geothermal power. <br><p> Energy left over from the original accretion of the planet and augmented by heat from radioactive decay seeps out slowly everywhere, everyday. In certain areas the geothermal gradient (increase in temperature with depth) is high enough to exploit to generate electricity. <br> This possibility is limited to a few locations on Earth and many technical problems exist that limit its utility. Another form of geothermal energy is Earth energy, a result of the heat storage in the Earth's surface. <br>Soil everywhere tends to stay at a relatively constant temperature, the yearly average, and can be used with heat pumps to heat a building in winter and cool a building in summer. This form of energy can lessen the need for other power to maintain comfortable temperatures in buildings, but cannot be used to produce electricity. <br>
Other forms of energy. <br><p>Energy from tides, the oceans and hot hydrogen fusion are other forms that can be used to generate electricity. Each of these is discussed in some detail with the final result being that each suffers from one or another significant drawback and cannot be relied upon at this time to solve the upcoming energy crunch. <br>
Can A Country Achieve 100% Renewable Energy? <br>
If you think 100% renewable energy will never happen, think again. <br> Several countries have adopted ambitious plan to obtain their power from renewable energy. These countries are not only accelerating RE installations but are also integrating RE into their existing infrastructure to reach a 100% RE mix. Read our article.. <br>
What are renewable energy sources? Solar power can be used directly for heating and producing electricity or indirectly via biomass, wind, ocean thermal, and hydroelectric power. <br>Energy from the gravititational field can be harnessed by tidal power; and the internal heat of the Earth can be tapped geothermally. <br>
<p>These tools and more can help make the transition from non-renewable to renewable and environmentally friendly energy. However, none of these is sufficiently developed or abundant enough to substitute for fossil fuels use. <br> Every one of these power sources (with the exception of hydroelectric) has low environmental costs, and combined have the potential to be important in avoiding a monumental crisis when the fossil fuel crunch hits. These energy sources are often non-centralized, leading to greater consumer control and involvement. <br>
<p>However, currently each of these energy forms is significantly more expensive than fossil fuels, which will lead to economic dislocations and hardship if they become the only power source for the future. </h3><br>



