<h1>Geothermal Energy</h1> <br>
<p><h3>Geothermal comes from the greek word Geo which means earth and thermal which means heat. So,Geothermal energy means energy or power extracted from beneath the earth. <br>The energy inside the earth was formed by the decay of minerals and forests several years ago. Traditionally, it was used for bathing and heating purposes but today it is also used for generating electricity. <br> Geothermal energy is called renewable source of energy because heat is continuously produced inside the earth. <br>

<p>This heat is brought to the near-surface by thermal conduction and by intrusion into the earth’s crust of molten magma originating from great depth. As groundwater is heated, geothermal energy is produced in the form of hot water and steam. <br>
Geothermal energy is produced inside the earth’s surface. The earth’s layer consists of innermost layer called Iron Core which itself has two layers: solid iron core and an outer core made of hard rock, called magma, mantle which surrounds the core and the outermost layer called crust which forms oceans and continents. <br>
When magma comes close to the earth’s surface i.e. crust it heats up the ground water which gets trapped in porous rocks. <br>They may also flow along faults and fractured rock surfaces. Now these hydrothermal resources have 2 ingredients: water (Hydro) and heat (thermal). When these hydrothermal resources occur naturally these are called Geothermal reservoirs. <br>Various tools and techniques are used nowadays to detect geothermal reservoirs.</h3> <br>
<h1>Geothermal Energy Existence</h1><br>
<p><h3>Geothermal energy exists in the form of: 1) Volcanoes 2) Hot Springs 3) Geysers
Geothermal energy is clean and sustainable and environment friendly. It may be noted that the so called ‘ring of fire’ of the Earth envelopes the Pacific rim. <br>Though there are over 300 hot springs sites in India, this form of energy is yet to be tapped. In USA, California generates highest amount of electricity through Geothermal energy. <br></h3>

<h1>Future Of Geothermal Energy</h1>
<p><h3>The future of geothermal energy depends on three factors: it’s demand, supply and it’s competitiveness among other renewable resources in terms of cost, availability, reliability etc..<br> Demand for geothermal energy is going to increase and increase with the increase in the population and extinction of other non-renewable sources. Moreover, today government also support the resources which are cleaner and do not spoil the environment. <br>
<p>Supply of geothermal energy is limited and confined to certain areas only. The entire resource of geothermal energy is fairly bigger than that of coal, oil and gas. <br> Geothermal energy can be made more widely available if the methods and technologies used to extract it are improved. Geothermal energy is still not explored fully. <br> Several miles below the earth surface is hot, dry rock being heated by the molten magma directly below it. <br></h3>




